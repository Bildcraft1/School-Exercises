# Calcolare l'area di un triangolo rettangolo dati i due cateti

# Variabili di Input
c1 = int(input("Digita il primo cateto: "))
c2 = int(input("Digita il secondo cateto: "))

# Formula da applicare
area = (c1*c2)/2

print("L'area di un triangolo rettangolo dato Cateto 1 =",c1,"e Cateto 2 =",c2,"vale:", area)

